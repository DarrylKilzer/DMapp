<template>
  <div class="conditions">
    <div class="pagecontent" data-pageid="5823">Conditions alter a creature’s capabilities in a variety of ways and can arise as a result of a spell, a class feature,
      a monster’s <a class="autolink" href="/compendium/dnd5e/Combat#h-Attack">Attack</a>, or other effect. Most conditions,
      such as blinded, are impairments, but a few, such as invisible, can be advantageous.<br><br>A condition lasts either
      until it is countered (the prone condition is countered by standing up, for example) or for a <a class="autolink" href="/compendium/dnd5e/Spells#h-Duration">Duration</a>      specified by the effect that imposed the condition.<br><br>If multiple effects impose the same condition on a creature,
      each instance of the condition has its own <a class="autolink" href="/compendium/dnd5e/Spells#h-Duration">Duration</a>,
      but the condition’s effects don’t get worse. A creature either has a condition or doesn’t.<br><br>The following definitions
      specify what happens to a creature while it is subjected to a condition.<br><br><a name="h-Blinded"></a><a name="toc_1"></a>
      <h2>Blinded</h2>
      <ul>
        <li>A blinded creature can’t see and automatically fails any ability check that requires sight.</li>
        <li>
          <a class="autolink" href="/compendium/dnd5e/Combat#h-Attack">Attack</a> rolls against the creature have advantage,
          and the creature’s <a class="autolink" href="/compendium/dnd5e/Combat#h-Attack">Attack</a> rolls have disadvantage.</li>
      </ul>
      <a name="h-Charmed"></a><a name="toc_2"></a>
      <h2>Charmed</h2>
      <ul>
        <li>A charmed creature can’t <a class="autolink" href="/compendium/dnd5e/Combat#h-Attack">Attack</a> the charmer or target
          the charmer with harmful <a class="autolink" href="/compendium/dnd5e/Sentient%20Magic%20Items#h-Abilities">Abilities</a>          or magical effects.</li>
        <li>The charmer has advantage on any ability check to interact socially with the creature.</li>
      </ul>
      <a name="h-Deafened"></a><a name="toc_3"></a>
      <h2>Deafened</h2>
      <ul>
        <li>A deafened creature can’t hear and automatically fails any ability check that requires hearing.</li>
      </ul>
      <a name="h-Fatigued"></a><a name="toc_4"></a>
      <h2>Fatigued</h2>See Exhaustion (below).<br><br><a name="h-Frightened"></a><a name="toc_5"></a>
      <h2>Frightened</h2>
      <ul>
        <li>A frightened creature has disadvantage on <a class="autolink" href="/compendium/dnd5e/Ability%20Scores#h-Ability%20Checks">Ability Checks</a>          and <a class="autolink" href="/compendium/dnd5e/Combat#h-Attack">Attack</a> rolls while the source of its fear
          is within line of sight.</li>
        <li>The creature can’t willingly move closer to the source of its fear.</li>
      </ul>
      <a name="h-Grappled"></a><a name="toc_6"></a>
      <h2>Grappled</h2>
      <ul>
        <li>A grappled creature’s speed becomes 0, and it can’t benefit from any bonus to its speed.</li>
        <li>The condition ends if the <a class="autolink" href="/compendium/dnd5e/Grappler#h-Grappler">Grappler</a> is incapacitated
          (see the condition).</li>
        <li>The condition also ends if an effect removes the grappled creature from the reach of the <a class="autolink" href="/compendium/dnd5e/Grappler#h-Grappler">Grappler</a>          or <a class="autolink" href="/compendium/dnd5e/Combat#h-Grappling">Grappling</a> effect, such as when a creature
          is hurled away by the <em><a class="autolink" href="/compendium/dnd5e/Thunderwave#h-Thunderwave">Thunderwave</a></em>          spell.</li>
      </ul>
      <a name="h-Incapacitated"></a><a name="toc_7"></a>
      <h2>Incapacitated</h2>
      <ul>
        <li>An incapacitated creature can’t take actions or reactions.</li>
      </ul>
      <a name="h-Invisible"></a><a name="toc_8"></a>
      <h2>Invisible</h2>
      <ul>
        <li>An invisible creature is impossible to see without the aid of magic or a <a class="autolink" href="/compendium/dnd5e/Weapons#h-Special">Special</a>          sense. For the purpose of <a class="autolink" href="/compendium/dnd5e/Ability%20Scores#h-Hiding">Hiding</a>, the
          creature is heavily obscured. The creature’s location can be detected by any noise it makes or any tracks it leaves.</li>
        <li>
          <a class="autolink" href="/compendium/dnd5e/Combat#h-Attack">Attack</a> rolls against the creature have disadvantage,
          and the creature’s <a class="autolink" href="/compendium/dnd5e/Combat#h-Attack">Attack</a> rolls have advantage.</li>
      </ul>
      <a name="h-Paralyzed"></a><a name="toc_9"></a>
      <h2>Paralyzed</h2>
      <ul>
        <li>A paralyzed creature is incapacitated (see the condition) and can’t move or speak.</li>
        <li>The creature automatically fails <a class="autolink" href="/compendium/dnd5e/Ability%20Scores#h-Strength">Strength</a>          and <a class="autolink" href="/compendium/dnd5e/Ability%20Scores#h-Dexterity">Dexterity</a> saving throws.</li>
        <li>
          <a class="autolink" href="/compendium/dnd5e/Combat#h-Attack">Attack</a> rolls against the creature have advantage.</li>
        <li>Any <a class="autolink" href="/compendium/dnd5e/Combat#h-Attack">Attack</a> that hits the creature is a critical
          hit if the attacker is within 5 feet of the creature.</li>
      </ul>
      <a name="h-Petrified"></a><a name="toc_10"></a>
      <h2>Petrified</h2>
      <ul>
        <li>A petrified creature is transformed, along with any nonmagical object it is wearing or carrying, into a solid inanimate
          substance (usually stone). Its weight increases by a factor of ten, and it ceases aging.</li>
        <li>The creature is incapacitated (see the condition), can’t move or speak, and is unaware of its surroundings.</li>
        <li>
          <a class="autolink" href="/compendium/dnd5e/Combat#h-Attack">Attack</a> rolls against the creature have advantage.</li>
        <li>The creature automatically fails <a class="autolink" href="/compendium/dnd5e/Ability%20Scores#h-Strength">Strength</a>          and <a class="autolink" href="/compendium/dnd5e/Ability%20Scores#h-Dexterity">Dexterity</a> saving throws.</li>
        <li>The creature has <a class="autolink" href="/compendium/dnd5e/Resistance#h-Resistance">Resistance</a> to all damage.</li>
        <li>The creature is immune to poison and disease, although a poison or disease already in its system is suspended, not
          neutralized.</li>
      </ul>
      <a name="h-Poisoned"></a><a name="toc_11"></a>
      <h2>Poisoned</h2>
      <ul>
        <li>A poisoned creature has disadvantage on <a class="autolink" href="/compendium/dnd5e/Combat#h-Attack">Attack</a> rolls
          and <a class="autolink" href="/compendium/dnd5e/Ability%20Scores#h-Ability%20Checks">Ability Checks</a>.</li>
      </ul>
      <a name="h-Prone"></a><a name="toc_12"></a>
      <h2>Prone</h2>
      <ul>
        <li>A prone creature’s only <a class="autolink" href="/compendium/dnd5e/Movement#h-Movement">Movement</a> option is to
          crawl, unless it stands up and thereby ends the condition.</li>
        <li>The creature has disadvantage on <a class="autolink" href="/compendium/dnd5e/Combat#h-Attack">Attack</a> rolls.</li>
        <li>An <a class="autolink" href="/compendium/dnd5e/Combat#h-Attack">Attack</a> roll against the creature has advantage
          if the attacker is within 5 feet of the creature. Otherwise, the <a class="autolink" href="/compendium/dnd5e/Combat#h-Attack">Attack</a>          roll has disadvantage.</li>
      </ul>
      <a name="h-Restrained"></a><a name="toc_13"></a>
      <h2>Restrained</h2>
      <ul>
        <li>A restrained creature’s speed becomes 0, and it can’t benefit from any bonus to its speed.</li>
        <li>
          <a class="autolink" href="/compendium/dnd5e/Combat#h-Attack">Attack</a> rolls against the creature have advantage,
          and the creature’s <a class="autolink" href="/compendium/dnd5e/Combat#h-Attack">Attack</a> rolls have disadvantage.</li>
        <li>The creature has disadvantage on <a class="autolink" href="/compendium/dnd5e/Ability%20Scores#h-Dexterity">Dexterity</a>          saving throws.</li>
      </ul>
      <a name="h-Stunned"></a><a name="toc_14"></a>
      <h2>Stunned</h2>
      <ul>
        <li>A stunned creature is incapacitated (see the condition), can’t move, and can speak only falteringly.</li>
        <li>The creature automatically fails <a class="autolink" href="/compendium/dnd5e/Ability%20Scores#h-Strength">Strength</a>          and <a class="autolink" href="/compendium/dnd5e/Ability%20Scores#h-Dexterity">Dexterity</a> saving throws.</li>
        <li>
          <a class="autolink" href="/compendium/dnd5e/Combat#h-Attack">Attack</a> rolls against the creature have advantage.</li>
      </ul>
      <a name="h-Unconscious"></a><a name="toc_15"></a>
      <h2>Unconscious</h2>
      <ul>
        <li>An unconscious creature is incapacitated (see the condition), can’t move or speak, and is unaware of its surroundings</li>
        <li>The creature drops whatever it’s holding and falls prone.</li>
        <li>The creature automatically fails <a class="autolink" href="/compendium/dnd5e/Ability%20Scores#h-Strength">Strength</a>          and <a class="autolink" href="/compendium/dnd5e/Ability%20Scores#h-Dexterity">Dexterity</a> saving throws.</li>
        <li>
          <a class="autolink" href="/compendium/dnd5e/Combat#h-Attack">Attack</a> rolls against the creature have advantage.</li>
        <li>Any <a class="autolink" href="/compendium/dnd5e/Combat#h-Attack">Attack</a> that hits the creature is a critical
          hit if the attacker is within 5 feet of the creature.</li>
      </ul>
      <br><a name="h-Exhaustion"></a><a name="toc_16"></a>
      <h2>Exhaustion</h2>
      <p>Some <a class="autolink" href="/compendium/dnd5e/Weapons#h-Special">Special</a> <a class="autolink" href="/compendium/dnd5e/Sentient%20Magic%20Items#h-Abilities">Abilities</a>        and environmental hazards, such as starvation and the long-&shy;term effects of freezing or scorching temperatures,
        can lead to a <a class="autolink" href="/compendium/dnd5e/Weapons#h-Special">Special</a> condition called exhaustion.
        Exhaustion is measured in six levels. An effect can give a creature one or more levels of exhaustion, as specified
        in the effect’s description.</p>
